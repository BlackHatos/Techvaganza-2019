package com.pnstech.finalactivity;

public class UserProfile
{
    public String userName;
    public String userEmail;
    public String userPhone;
    public String  userCollege;
    public String  userSemester;

    public UserProfile(String userName, String userEmail, String userPhone, String userCollege, String userSemester) {
        this.userName = userName;
        this.userEmail = userEmail;
        this.userPhone = userPhone;
        this.userCollege = userCollege;
        this.userSemester = userSemester;
    }
}
